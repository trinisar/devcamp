const axios = require('axios');

const fetchData = async () => {
    try {
        const response = await axios.get('https://dev.codekit.co/devcamp-api/covid-country.txt');
      
        const allCovidData = response.data;
        
        // const todayRecovered = allCovidData.reduce((sum, country) => sum + country.todayRecovered, 0);
        // console.log(todayRecovered);

        const result = allCovidData
            .filter(country => country.todayDeaths < 10 && country.population > 100000000)
            .map(country => country.country)
        
        console.log(result)

    } catch (error) {
        console.error('Error fetching data:', error);
    }
}

fetchData();
